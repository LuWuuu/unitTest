#include "Calcu.h"

double Calcu::Add(double number1, double number2) {
	double result = number1 + number2;
	return result;
}
double Calcu::Sub(double minuend, double subtrahend) {
	double result = minuend - subtrahend;
	return result;
}
double Calcu::Multi(double number1, double number2) {
	double result = number1 * number2;
	return result;
}
double Calcu::Divide(double dividend, double divisor) {
	double result = dividend / divisor;
	return result;
}
